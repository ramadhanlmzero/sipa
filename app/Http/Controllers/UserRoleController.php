     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        try {
            $user = User::with('cRoles')->find($id);
            $roleother = Role::all();
            $data = [
                'user' => $user,
                'roleother' => $roleother
            ];
            return $data;
        } 
        catch (ValidationException $exception) {
            return response()->json([
                'status' => 'error',
                'msg'    => 'Error',
                'errors' => $exception->errors(),
            ], 422);
        }
    }

